class Keyb < Formula
  desc "Echoes individual bytes generated by keystrokes"
  homepage "https://github.com/davidledwards/keyb"
  version "0.4.0"
  url_prefix = "https://github.com/davidledwards/keyb/releases/download/v#{version}/keyb-#{version}"

  on_macos do
    on_arm do
      url "#{url_prefix}-aarch64-apple-darwin.tar.gz"
      sha256 "bf601d1a7c331594625569e840675801d18a645a8494aa11cfdcf7070b187400" # aarch64-apple-darwin
    end
    on_intel do
      url "#{url_prefix}-x86_64-apple-darwin.tar.gz"
      sha256 "907d731410beb1193ea264292a107516c992249f834cc8a585649e287b034280" # x86_64-apple-darwin
    end
  end

  on_linux do
    on_intel do
      if Hardware::CPU.is_64_bit?
        url "#{url_prefix}-x86_64-unknown-linux-gnu.tar.gz"
        sha256 "907d731410beb1193ea264292a107516c992249f834cc8a585649e287b034280" # x86_64-unknown-linux-gnu
      else
        url "#{url_prefix}-i686-unknown-linux-gnu.tar.gz"
        sha256 "907d731410beb1193ea264292a107516c992249f834cc8a585649e287b034280" # i686-unknown-linux-gnu
      end
    end
  end

  def install
    bin.install "keyb"
  end

  test do
    system "#{bin}/keyb", "--version"
  end
end
