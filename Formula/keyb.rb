class Keyb < Formula
  desc "Echoes individual bytes generated by keystrokes"
  homepage "https://github.com/davidledwards/keyb"
  version "0.5.0"
  url_prefix = "https://github.com/davidledwards/keyb/releases/download/v#{version}/keyb-#{version}"

  on_macos do
    on_arm do
      url "#{url_prefix}-aarch64-apple-darwin.tar.gz"
      sha256 "7066738cdfeab860ebc1a2e0980029fdd9b4ac31c42d2247a442e5768d7617b4" # aarch64-apple-darwin
    end
    on_intel do
      url "#{url_prefix}-x86_64-apple-darwin.tar.gz"
      sha256 "ceb523adf15da62d7cabdbbb73a61bbc31bf81834838c588e3cb65fc14e3b959" # x86_64-apple-darwin
    end
  end

  on_linux do
    on_intel do
      if Hardware::CPU.is_64_bit?
        url "#{url_prefix}-x86_64-unknown-linux-gnu.tar.gz"
        sha256 "589d86a5c5fb9494596eb0654f17eba3b44baeed731bff9b67aa14c146a3fd3d" # x86_64-unknown-linux-gnu
      else
        url "#{url_prefix}-i686-unknown-linux-gnu.tar.gz"
        sha256 "41515fa9fd71560faf29a7d9d8d539a2259da4bf85e83ce420ba915cb0518328" # i686-unknown-linux-gnu
      end
    end
  end

  def install
    bin.install "keyb"
  end

  test do
    system "#{bin}/keyb", "--version"
  end
end
